/* Defines from mlkem_native.h */

#define MLKEM768_CIPHERTEXTBYTES	1088
#define MLKEM768_SYMBYTES			32
#define MLKEM768_BYTES				32

/* The ML-KEM key exchange process is pretty peculiar because it acts like a
   kind of reverse-RSA, with the client providing the public key and the
   server encrypting a session key with it.  However since a new public key
   has to be generated each time it's also partly like Diffie-Hellman.  Or,
   to put it another way, like anything PQC it doesn't look like anything 
   normal.
   
   The sequence of operations is:

	A.load:		generate privK, pubK
				write pubK

	B.wrap		read pubK
				generate session-key
				wrap session-key using pubK

	A.unwrap	read wrapped key
				unwrap session-key using privK

   See FIPS 2.30 section 3 for details */

